  <h1> ¿Como puedo Cambiar mis prendas?</h1>
    <br>
    <h4>Si la prenda que recibiste no es exactamente como la esperabas, podés cambiarla.</h4>
    <br>
<h4>Estos son los pasos:
<br>
1) Una vez que el pedido se da como "Completado", seleccioná el botón CAMBIOS en la sección Mis Compras. Este botón sólo va a estar por 48 hs corridas!
<br>
2) Elegí qué producto querés cambiar y el motivo del cambio.
<br>
3) Embalá los productos que vayas a cambiar.
<br>
4) Imprimí la solicitud de cambio y agregala en el paquete.
<br>
5) Hacenos llegar el paquete a nuestra oficina en Migueletes 1987, Oficina 1, Belgrano, CABA [tenés 10 días corridos desde que generaste el cambio].
<br>
Estamos de Lunes a Viernes de 10 a 18 hs. Podés venir vos misma ó enviarlo por el medio que quieras.
<br>
6) Una vez que lo recibamos, vamos a procesar el cambio y validar el motivo que seleccionaste. Luego recibirás tu cupón de cambio ó nos contactaremos para informarte pasos a seguir.
<br></h4>
<h4>Aclaración: Este botón de Cambios va a estar durante 48 horas. Te vas a dar cuenta que se dio como completado porque en ese momento, Renová Tu Vestidor te va a mandar un mail para calificar a tu vendedora.
<br>
IMPORTANTE: El costo del envío es a cargo de la compradora. Recordá por favor que no podemos pagar estos envíos. Si vos despachás el paquete con pago en destino, el mismo volverá al remitente.</h4>  </body>
